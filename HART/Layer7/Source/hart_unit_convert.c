//call with HART-defined unitCode and (maybe unknown) System-defined unitTypeCode
//success-return with System-defined unitCode and unitTypeCode
TBOOL HartConvUnitCodeFromHartUnit_HART(TUSIGN8 * pUnitCode, TUSIGN8 * pUnitTypeCode )
{
    TUSIGN8 unitTypeCounter;
    TUSIGN8 unitCounter;

    if( pUnitCode && pUnitTypeCode)
    {
        //value of NOT_SUPPORTED must be invalid at HART-Spec to have an exclusive identification...
        if(*pUnitCode != NOT_SUPPORTED)
        {
            //...and search makes sense
            for(unitTypeCounter = 0; unitTypeCounter < (sizeof(TypeTab)/sizeof(TypeTab[0])); unitTypeCounter++)
            {
                //value of HART_UM_T_UNKNOWN_UNIT_GROUP signs to search in all groups, first occurence bingo
                //this direction (HART-Unit to System-Unit) shall be 'only one occurence', because
                //the HART-Unit definition implicits the Unit-Group
                if( *pUnitTypeCode != UM_T_UNKNOWN_UNIT_GROUP )
                {
                    // a stated systemUnitsTypeCode...
                    if( TypeTab[unitTypeCounter].systemUnitsType != *pUnitTypeCode )
                    {
                        //...speeds up search
                        continue;
                    }
                }
                for(unitCounter = 0; unitCounter < TypeTab[unitTypeCounter].numUnits; unitCounter++)
                {
                    if(TypeTab[unitTypeCounter].pUnitDescr[unitCounter].hartUnit == *pUnitCode)
                    {
                        //return System-Type
                        *pUnitTypeCode = TypeTab[unitTypeCounter].systemUnitsType;
                        //return System-Unit
                        *pUnitCode = TypeTab[unitTypeCounter].pUnitDescr[unitCounter].systemUnit;
                        return eTRUE;
                    }
                }
            }
        }
    }
    return eFALSE;
}

