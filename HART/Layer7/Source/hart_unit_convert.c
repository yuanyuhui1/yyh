//call with System-defined unitCode and unitTypeCode
//success-return with HART-defined unitCode
TBOOL HartConvUnitCodeFromSystemUnit_HART(TUSIGN8 * pUnitCode, TUSIGN8 * pUnitTypeCode)
{
    TUSIGN8 unitTypeCounter;
    TUSIGN8 unitCounter;

    if( pUnitCode && pUnitTypeCode )
    {
        //TODO if((*pUnitCode != NOT_SUPPORTED) && (*pUnitTypeCode != UM_T_UNKNOWN_UNIT_GROUP))
        {
            for(unitTypeCounter = 0; unitTypeCounter < (sizeof(TypeTab)/sizeof(TypeTab[0])); unitTypeCounter++)
            {
                if( TypeTab[unitTypeCounter].systemUnitsType != *pUnitTypeCode )
                {
                    continue;
                }
                for(unitCounter = 0; unitCounter < TypeTab[unitTypeCounter].numUnits; unitCounter++)
                {
                    if(TypeTab[unitTypeCounter].pUnitDescr[unitCounter].systemUnit == *pUnitCode)
                    {
                        //return HART-Unit
                        *pUnitCode = TypeTab[unitTypeCounter].pUnitDescr[unitCounter].hartUnit;
                        if( *pUnitCode == NOT_SUPPORTED )
                        {
                            *pUnitCode = HART_UM_UKNOWN_UNIT;
                        }
                        return eTRUE;
                    }
                }
            }
        }
    }
    return eFALSE;
}
